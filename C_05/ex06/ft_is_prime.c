int		ft_is_prime(int nb)
{
	int i;
	int prime;

	i = 2;
	prime = 1;
	if (nb <= 1)
	{
		return (0);
	}
	while (i < nb)
	{
		if (nb % i == 0)
			prime = 0;
		i++;
	}
	return (prime);
}
